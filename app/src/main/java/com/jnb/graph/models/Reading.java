package com.jnb.graph.models;

public class Reading {
    public float xValue;
    public int xBoxes;
    public float yValue;
    public int yBoxes;

    public Reading(float xValue, int xBoxes, float yValue, int yBoxes) {
        this.xValue = xValue;
        this.xBoxes = xBoxes;
        this.yValue = yValue;
        this.yBoxes = yBoxes;
    }
}
