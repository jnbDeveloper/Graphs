package com.jnb.graph.models;

public class ReadingSpecialDetails {
    public float xMin;
    public float xMax;
    public float yMin;
    public float yMax;

    public ReadingSpecialDetails(float xMin, float xMax, float yMin, float yMax) {
        this.xMin = xMin;
        this.xMax = xMax;
        this.yMin = yMin;
        this.yMax = yMax;
    }
}
